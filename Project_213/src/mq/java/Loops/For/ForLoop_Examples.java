package mq.java.Loops.For;

public class ForLoop_Examples {

	public static void main(String[] args) 
	{
		//Print numbers from 1 to 10
		for (int i = 1; i < 11; i++) 
		{
			System.out.println(i);
		}
		
		
		//Print numbers from 10 to 1
		for (int i = 10; i > 0; i--) 
		{
			System.out.println(i);
		}
		
		
		

	}

}
